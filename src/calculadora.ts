const sumar = (numeroUno: number, numeroDos: number) => numeroUno + numeroDos
const restar = (numeroUno: number, numeroDos: number) => numeroUno - numeroDos
const multiplicar = (numeroUno: number, numeroDos: number) => numeroUno * numeroDos
const dividir = (numeroUno: number, numeroDos: number) => numeroUno / numeroDos

export { sumar, restar, multiplicar, dividir }
